def main():
    # Инициализация словаря для хранения адресов
    addresses = {}

    while True:
        # Отображение меню действий
        print("\nМеню:")
        print("1. Добавить запись")
        print("2. Изменить адрес")
        print("3. Удалить запись")
        print("4. Просмотреть все записи")
        print("5. Выйти")

        # Запрос выбора действия у пользователя
        choice = input("Выберите действие (1-5): ")

        if choice == '1':
            # Добавление новой записи
            name = input("Введите имя: ")  # Ввод имени
            address = input("Введите адрес: ")  # Ввод адреса
            addresses[name] = address  # Сохранение записи в словаре
            print(f"Запись добавлена: {name} - {address}")

        elif choice == '2':
            # Изменение адреса существующей записи
            name = input("Введите имя для изменения адреса: ")
            if name in addresses:  # Проверка, существует ли запись
                new_address = input("Введите новый адрес: ")  # Ввод нового адреса
                addresses[name] = new_address  # Обновление адреса в словаре
                print(f"Адрес для {name} изменён на {new_address}.")
            else:
                print("Запись не найдена.")

        elif choice == '3':
            # Удаление записи
            name = input("Введите имя для удаления записи: ")
            if name in addresses:  # Проверка, существует ли запись
                del addresses[name]  # Удаление записи из словаря
                print(f"Запись для {name} удалена.")
            else:
                print("Запись не найдена.")

        elif choice == '4':
            # Просмотр всех записей
            if addresses:  # Проверка, есть ли записи в словаре
                print("Словарь адресов:")
                for name, address in addresses.items():  # Перебор всех записей
                    print(f"{name}: {address}")  # Вывод имени и адреса
            else:
                print("Словарь пуст.")

        elif choice == '5':
            # Выход из программы
            print("Выход из программы.")
            break  # Завершение цикла и выхода из программы

        else:
            # Обработка неверного выбора
            print("Неверный выбор. Попробуйте снова.")

# Запуск основной функции, если файл выполняется как скрипт
if __name__ == "__main__":
    main()

